#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char a;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	if (n <= 9 && n >= 0)
	{
		a = '0' + n;
		write(fd, &a, 1);
	}
}
